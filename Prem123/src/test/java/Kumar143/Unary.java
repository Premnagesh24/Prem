package Kumar143;

public class Unary {

	public static void main(String[] args) {
  
	int a =10;
	System.out.println(a);
	
	int b =++a;
	System.out.println(b);
	
	int c =b++;
	System.out.println(c);
	
	int x =20;
	System.out.println(x);
	
	int y =--x;
	System.out.println(y);
	
	int z =y++;
	System.out.println(z);
		
		
	}

}
